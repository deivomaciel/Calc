 const add = (numberA, numberB) => {
    return numberA + numberB
}

export const subtract = (numberA, numberB) => {
    return numberA - numberB
}

export const multiply = (numberA, numberB) => {
    return numberA * numberB
}

export const divide = (numberA, numberB) => {
    return numberA / numberB
}
  
export const operations = {
    add,
    subtract,
    multiply,
    divide
  }